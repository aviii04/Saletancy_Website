                <h3>BEST PRACTICES</h3>
            </header>
        <hr class="featurette-divider">
        <p >
            DO NOT send from your own domain – Set up the .co or .net version of your own domain using a cheap (~$10/year) domain registrar like <a href="https://www.namecheap.com/" style="color:18a26e;">NameCheap</a>. Especially when starting out, you might make some mistakes, and you don’t want that to come back and affect your ability to send emails from your company’s main domain. If you do manage to get your secondary domain flagged as spam or blacklisted, it’s not the end of the world. Set up another new domain name and don’t make the same mistakes again.
        </p><br>
        <p >
            Use Google Apps – All of these tactics will work best when sent from an account that can use Gmail. Once you have your new domain, set up a <a href="https://gsuite.google.com/" style="color:18a26e;">Google Apps account</a>. The first month is free, and $5/month after that. The first month will have lower sending limits, but we recommend taking things slow in the first month anyways, so you get the hang of things properly.
        </p><br>
        <p >
            Set up SPF and DKIM records – This is how your email address will be authenticated, so recipient email servers know it can be trusted. Without this, your emails could go to spam without you doing anything else wrong. If you have a paid Google Apps account, you can actually call Google directly and talk to a real person for support, and they can help walk you through these steps. Or if you’re technically inclined, you can do it yourself following these directions from Google for setting up <a href="https://support.google.com/a/answer/178723?hl=en" style="color:18a26e;">SPF</a> and <a href="https://support.google.com/a/answer/174124?hl=en" style="color:18a26e;">DKIM records.</a>
        </p><br>
        <p >
            Comply with CAN-SPAM – This one is very important. While it is unlikely you will be reported if your break the rules of CAN-SPAM, you can be fined up to $16,000 for every complaint. The rules are very easy to follow, so make sure you comply. Here they are:<br>
            <strong>•   Don’t use false or misleading header information </strong>– This is different from misrepresenting your subject line. This means don’t put “re:” or “fwd:” in the subject line to trick the recipient into thinking the message is from an ongoing conversation.<br>
            <strong>•   Don’t use deceptive subject lines </strong>– Basically, don’t use the subject line “You’ve won a new car!” if you’d like to get your prospect on the phone to discuss your marketing services. Not only is this illegal, but this type of bait and switch would never work anyways.<br>
            <strong>•   Identify the message as an ad </strong> – The law is a bit loose here, but the main point is that you need to clear about what your intent is. You don’t have to come right out and say, “I want you to become my client and pay me money”, but you should definitely say you’d like to have a quick intro call to see if there’s a mutual fit.<br>
            <strong>•   Tell recipients where you’re located</strong> – This is best accomplished by using your business address in your signature.<br>
            <strong>•   Tell recipients how to opt out of receiving future email from you</strong>– We recommend against actual unsubscribe links. They are difficult to set up, and can cause things to go to Promotions tab in Gmail, or even worse, the spam folder. It’s enough to include a small line in your signature that says something like “Don’t want to receive emails from me? Just reply back and let me know.”<br>
            <strong>•   Honor opt-out requests promptly</strong> – When someone asks to be removed from your list, don’t even email them again. Very simple.<br>
            <strong>•   Monitor what others are doing on your behalf </strong>– If you’re working with a third party to send emails on your behalf, make sure they also compliant with CAN-SPAM, as you’ll both be liable for any infractions. When working with clients (and for our our own campaigns), we are always 100% CAN-SPAM compliant.<br>
            You can see a more detailed overview of the CAN-SPAM laws <a href="" style="color:18a26e;">here</a>.
        </p>
